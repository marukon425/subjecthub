package basic_arithmetic_operations;


// for文
public class Test13 {
    public static void main(String[] args) {
        // int i = 0 ⇒ 「i」という変数を初期値0として用意する
        // i < 10 ⇒ i が10になるまで繰り返す
        // i++ ⇒ iを1づつ増やす
        for (int i = 0; i < 10; i++);
        System.out.println("こんにちは");
    }
}



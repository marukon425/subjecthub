# 1~10までのランダム数値を表示
# ランダムモジュールをインポートする
import random
# 表示
print(random.randint(1,10))

# =============================================================================


# ランダムモジュールを指定せずにrandintをインポートする
from random import randint
# 表示
print(randint(1,10))

# =============================================================================

# ランダムモジュールを指定せずにrandintをインポートする
# ［ri］と別名をつける
from random import randint as ri
# 表示
print(ri(1,10))



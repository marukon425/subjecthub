// fscan.go
// したいこと：データファイル（sample.dat）を新しく作る5つの動物の名前を、番号付きのリストにして保存する
/*
データの用意:「Dog, Cat, Pig, Deer, Raccoons」という5つの文字データを用意します。
ファイルの新規作成:パソコンの中に sample.dat という名前のファイルを自動で作ります。
データの書き込み:そのファイルの中に、以下のような形式で1行ずつ文字を書き残そうとしています。
*/
package main

import (
	"fmt"
	"log"
	"os"
)

func main() {
	// 扱うファイル名と、書き込む文字列の配列（スライス）を準備
	fname := "sample.dat"
	words := [...]string{"Dog", "Cat", "Pig", "Deer", "Raccoons"}

	// 【ステップ1】ファイルを作成して書き込む準備
	// os.Openは通常「読み込み専用」のため、第2引数にオプショフラグを渡せる os.OpenFile を使うのが一般的ですが、
	// ここではファイルを開く処理を行っています。（※エラー時はログを出して強制終了）
	ofile, oerr := os.OpenFile(fname, os.O_RDWR|os.O_CREATE, 0666)
	if oerr != nil {
		log.Fatal(oerr)
	}

	// 【ステップ2】ループを使ってファイルにデータを書き込む
	// fmt.Fprintfで画面ではなく「ofile（ファイル）」に出力先を指定しています。
	// "%03d" は「数値を3桁で表示し、空いた枠は0で埋める（例: 001）」という意味です。
	for i, w := range words {
		fmt.Fprintf(ofile, "%03d: %s\n", i+1, w)
	}

	// 【ステップ3】書き込みが終わったので一度ファイルを閉じる
	ofile.Close()

	// 【ステップ4】データを読み込むために、今度は「読み込み専用」でファイルを開く
	ifile, ierr := os.OpenFile(fname, os.O_RDONLY, 0444)
	if ierr != nil {
		log.Fatal(ierr)
	}

	// 【ステップ5】ファイルからデータを1行ずつ読み込んで画面に表示する
	var v int      // 読み込んだ数値を格納する変数
	var str string // 読み込んだ文字列を格納する変数
	for {
		// fmt.Fscanfでファイルから指定した形式（数値: 文字列）のパターンを探して読み込みます。
		// &v や &str のように「&」を付けることで、変数に値を直接書き換えてもらいます。
		n, err := fmt.Fscanf(ifile, "%d: %s\n", &v, &str)

		// 読み込んだデータが0件（終わり）、または何らかのエラー（EOFなど）が発生したらループを抜けます
		if n == 0 || err != nil {
			break
		} else {
			// 読み込みに成功したデータを、画面（標準出力）に3桁形式で表示します
			fmt.Printf("%03d: %s\n", v, str)
		}
	}

	// 【ステップ6】最後にファイルを閉じて、プログラムを終了する
	ifile.Close()
}

/*
① fname （どのファイル？）事前に準備した "sample.dat" というファイル名が入っています。
「これから sample.dat というファイルに対して作業するよ」と指定しています。
② os.O_RDWR | os.O_CREATE （どんなモードで？）ここは |（縦棒）で2つの注文を合体させています。
os.O_RDWR（読み書きモード）: 「読む（Read）ことも、書く（Write）ことも両方できるようにして！」という注文です。
os.O_CREATE（新規作成モード）: 「もし指定したファイルがまだパソコンの中に無かったら、新しく作ってね！」という注文です。
③ 0666 （誰が使っていいの？）これは「パーミッション（権限）」と呼ばれるファイルのセキュリティ設定です。
0666 は「このパソコンを使う人なら、誰でもこのファイルを読んだり書き換えてもいいよ」という一番オープンな設定を意味しています。
*/

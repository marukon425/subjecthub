// bufferio.go
package main

// 【import構文】このプログラムで使う外部の道具箱（パッケージ）を読み込む
import (
	"bufio"   // ファイルを「行単位」でスマートに処理する道具
	"fmt"     // 画面に文字を表示（Print）する道具
	"os"      // ファイルの作成（Create）や開封（Open）など、OSを動かす道具
	"strconv" // String型とInt型など、「型」を変換（Convert）する道具
)

// 【main関数】プログラムを実行したときに、最初に自動で呼び出される特別な関数
func main() {
	// 【型推論の代入 `:=`】右側の文字列から自動で「string型」と判定して fname 変数を作る
	// `../` は「今いるフォルダの1つ上のフォルダ」という意味（相対パス）
	fname := "../sample.dat"

	// 【変数の宣言 `var`】後で使うための「空のハコ」を用意する
	var file *os.File // `*os.File` は「ファイルそのものの場所」を指すポインタ型
	var err error     // `error` は「エラー情報（中身がなければnil）」を入れるエラー型

	// ─── ✨ 【前半戦】ファイルへの書き込み処理 ───

	// 【多値返却（関数の戻り値を2つ同時に受け取る）】
	// os.Create関数は「ファイルのハコ」と「エラー情報」の2つを同時に返すので、カンマで並べて受け取る
	file, err = os.Create(fname)

	// 【if文によるエラーハンドリング】
	// err が `nil`（何もエラーがない空の状態）「ではない（!=）」、つまりエラーが発生したときの処理
	if err != nil {
		// `_ =` は「この戻り値は今回は無視して使わないよ」というGo言語特有のルール（ブランク識別子）
		_ = fmt.Errorf("%sを開けません。", fname)
	}

	// 【文字列変数の宣言】空っぽの文字列（string型）のハコを先に用意
	var txt string

	// 【forループ文】
	// i := 0 (カウンターを0からスタート)
	// i < 5 (iが5より小さい間、つまり i=0,1,2,3,4 の合計5回繰り返す)
	// i++ (1周終わるごとに i に 1 を足す)
	for i := 0; i < 5; i++ {

		// 【文字列の結合と型変換】
		// Go言語では「文字」と「数字のi」をそのまま `+` で足せないので、
		// `strconv.Itoa(i + 1)` を使って、計算用の数字を「"1" や "2" という文字」に変換してから `+` で合体させている
		txt = "これはサンプルテキスト" + strconv.Itoa(i+1) + "行目\n"

		// 【型キャスト（型の変換）】
		// `file.Write`関数は、文字（string型）のままだとファイルに書き込めないルール
		// そのため、`[]byte(txt)` と書いて「バイトデータ（パソコンが読めるデータの塊）型」に強制変身させて渡している
		file.Write([]byte(txt))
	}

	// 【ファイルのクローズ】
	// 書き込み用として開いていたファイルを閉じて、パソコンに「保存完了したよ」と伝える
	file.Close()

	// ─── 📖 【後半戦】ファイルからの読み込み処理 ───

	// os.Open関数で、さっき保存したファイルを今度は「読み込み専用」として開き直す
	file, err = os.Open(fname)
	if err != nil {
		_ = fmt.Errorf("%sを開けません。", fname)
	}

	// 【defer文（遅延実行）】
	// この `main` 関数がすべて終わって終了する直前に、自動で `file.Close()` を実行してね、という強力な予約機能
	defer file.Close()

	// 【構造体の生成】
	// bufio.NewScanner関数にファイルを渡して、ファイルを1行ずつスキャンしてくれる「Scanner型のオブジェクト(sc)」を作る
	sc := bufio.NewScanner(file)

	// 【forによる条件ループ】
	// `sc.Scan()` を実行すると、ファイルから次の1行を読み込む。
	// 次の行が読めたら `true` を返すのでループが続き、ファイルの最後までいって行がなくなると `false` になって自動でループが終了する
	for sc.Scan() {

		// 【if文の中での変数宣言】
		// if文の条件式の中で `err := sc.Err()` として、スキャン中にエラーが起きなかったかをその場でチェックしている
		if err := sc.Err(); err != nil {
			_ = fmt.Errorf("%sを読み込めません。", fname)
			break // エラーが起きたら `for` ループを途中で強制脱出（終了）する
		}

		// 【データの取り出しと出力】
		// `sc.Text()` で、今スキャンして手元に持っている「1行分の文字列」を抜き出して、
		// `fmt.Println` で画面に表示する（Printlnは最後に自動で改行を入れてくれる関数）
		fmt.Println(sc.Text())
	}
}

// iou.go
/*
このプログラムの目的は、「一番シンプルに、ファイルへの保存と読み込みを行う方法を学ぶこと」です。
このコードは、Go言語の標準ライブラリの中でも、昔からよく使われていた「ioutil」パッケージを使っています。
このパッケージは、ファイルの読み書きを簡単にするための便利な関数がたくさん入っています。
ただし、最近のGo言語では「os」パッケージを使うことが推奨されている。
*/
package main

import (
	"fmt"
	"io/ioutil" // 【解説】昔のGo言語でよく使われていた、ファイルの読み書きを簡単にするパッケージだよ！（今は os パッケージを使うのがナウいよ）
)

func main() {
	fname := `sample.txt`

	// ファイルへの書き込み
	txt := "これはサンプルテキスト"
	// 書き込む
	// 【解説】ioutil.WriteFile は「ファイルを開く ➔ 文字を書く ➔ ファイルを閉じる」を1行でやってくれる超お手軽な関数だよ！
	// 【解説】[]byte(txt) は、文字列をコンピューターが扱いやすい「バイト列（データの塊）」に変換しているよ。
	werr := ioutil.WriteFile(fname, []byte(txt), 0666)
	if werr != nil {
		// 【注意】fmt.Errorf は文字を作るだけで画面には表示されないよ。レポート等では fmt.Printf や log.Fatal に直すとGood！
		_ = fmt.Errorf("%sに書き込めません", fname)
	}

	// ファイルからの読み込み
	// 【解説】ioutil.ReadFile も、1行書くだけで指定したファイルの中身を丸ごと全部読み込んでくれる便利な関数だよ！
	data, rerr := ioutil.ReadFile(fname)
	if rerr != nil {
		// 【注意】ここも上と同じで、エラーが起きても画面に何も表示されない状態になっているから気をつけてね。
		_ = fmt.Errorf("%sから読み込めません", fname)
	} else {
		// 【解説】読み込んだデータ（バイト列）を、人間が読める「文字列（string）」に変換して画面に表示しているよ！
		fmt.Printf(string(data))
	}
} // 【解説】最後に main 関数を閉じる括弧（ } ）が足りなかったので追加したよ！

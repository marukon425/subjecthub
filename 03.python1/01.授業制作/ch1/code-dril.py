'''
数値１を入力
数値２を入力
〇と〇の合計は〇です
'''
# 数値１を変数num1に入力(input) num =number
num1 = input("数値１を入力>>>")

# 数値2を変数num2に入力(input)
num2 = input("数値２を入力>>>")

# 結果を表示(print)  
print(num1, "と", num2, "の合計は", num1 + num2, "です" )

# ======注意=======
# inputで入力すると文字型
# 文字型+文字型＝文字の結合
# 【解決策】文字型を数値型に変換！



# print関数(ターミナルに表示)の学習

# ""なし：定数(数値)として判断
print(1 + 1)
print(10 - 2)

# ""あり：文字として判断
print("1 + 1")
print("10 - 2")

# 　+：数値の場合は「加算」、文字の場合は「結合」
print("1" + "1")

# 3 * 4を計算 数値なので計算
print(3*4)

# "3" *4を実行　文字なので反復(繰り返し)
print("3"*4)

# 今日は晴れ　と表示
print("今日は晴れ")

# 今日は(改行して)晴れ　と表示　改行：\n
print("今日は\n晴れ")
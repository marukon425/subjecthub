# 二次元リスト (表形式) の学習

# リスト変数 a に 1,2,3 を設定
a = [1, 2, 3]
# リスト変数 b に 4,5,6 を設定
b = [4, 5, 6]
# リスト変数 c に a, b を設定
c = [a, b]
# cの0番目にはリストa　1番目にはリストb

# リストｃを表示
print (f"リストc：{c}")

# cの中の　1　を表示したい
print (f"リスト c の 0番目 のデータ：{c[0][0]}")

# cの中の 5 を表示
print (f"リスト c の 5番目 のデータ：{c[1][1]}")

# リスト構造の学習(基本情報では「配列」)
'''
リストとは、、、
１つの変数で複数のでーたを管理
添え字(管理番号)でデータの位置を特定
'''

# リストの作成　　(変数の場合　変数名＝代入したい値)
# 変数名＝［〇〇、△△、▢▢］
# リストに「工藤、松田、浅黄」を設定
# リストの順番は必ず０から始まる！！！

#           0       1       2   
member = ["工藤", "丸山", "浅木"]

# memberを表示
print(member)

# 工藤を表示　※memberの0番目
print(member[0])

# memberを１番目を自分の名前に上書き

# memberを表示

# memberの３番目を表示
print(member[2])